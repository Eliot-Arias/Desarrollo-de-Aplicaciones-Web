<?php
class Operacion{
    public static function suma($n1, $n2){
        return $n1 + $n2;
    }
    public static function resta($n1, $n2){
        return $n1 - $n2;
    }
    public static function division($n1, $n2){
        return $n1 / $n2;
    }
    public static function multiplicacion($n1, $n2){
        return $n1 * $n2;
    }
    public static function modulo($n1, $n2){
        return $n1 % $n2;
    }
}
?>